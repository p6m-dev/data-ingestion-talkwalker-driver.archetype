class LogMetricsConstants:
    """LogConstants holds all constants used in this package"""

    # usage log_level == LOG_LEVEL_INFO or LOG_LEVEL_ERROR or LOG_LEVEL_ALL
    LOG_LEVEL_INFO = 0x01
    LOG_LEVEL_ERROR = 0x02
    LOG_LEVEL_ALL = LOG_LEVEL_INFO | LOG_LEVEL_ERROR

    # usage log_destination == LOG_DESTINATION_LOCAL or LOG_DESTINATION_OBJECT_STORE or
    #                          LOG_DESTINATION_CLOUD_LOGS or LOG_DESTINATION_ALL
    LOG_DESTINATION_LOCAL = 0x01
    LOG_DESTINATION_OBJECT_STORE = 0x02
    LOG_DESTINATION_CLOUD_LOGS = 0x04
    LOG_DESTINATION_ALL = LOG_DESTINATION_LOCAL | LOG_DESTINATION_OBJECT_STORE | LOG_DESTINATION_CLOUD_LOGS

    # usage metrics_destination == METRICS_DESTINATION_LOCAL or  METRICS_DESTINATION_CLOUD or
    #                              METRICS_DESTINATION_ALL
    METRICS_DESTINATION_LOCAL = 0x01
    METRICS_DESTINATION_CLOUD = 0x02
    METRICS_DESTINATION_ALL = METRICS_DESTINATION_LOCAL | METRICS_DESTINATION_CLOUD
    LOG_BACKUP_DIR = './log_backup'